package com.demo1.model;

/**
 * 坐标的get和set方法
 *
 * @author admin
 */
public class Coord {
    int x;
    int y;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

}
